package scramble.model.map.util.enums;

/**
 * enum that describes the behaviour of the Landscape.
 */
public enum LandBehaviour {
    /**
     * Flat landscape.
     */
    FLAT,
    /**
     * Ascending landscape.
     */
    UP,
    /**
     * Descending landscape.
     */
    DW,
    /**
     * Summit landscape.
     */
    SUMMIT,
    /**
     * Landscape not present.
     */
    EMPTY
}
